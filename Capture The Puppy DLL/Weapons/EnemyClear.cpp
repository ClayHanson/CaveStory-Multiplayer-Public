void ActBullet_EnemyClear(BULLET* bul, int level)
{
	if (++bul->count1 > bul->life_count)
	{
		bul->cond = 0;
	}
	else
	{
		bul->damage  = 10000;
		bul->enemyXL = 0xC8000;
		bul->enemyYL = 0xC8000;
	}
}